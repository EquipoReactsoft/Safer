package rs.com.safer.Models;

public class UserUnidad {
    private String email, status;

    public UserUnidad() {
    }

    public UserUnidad(String email, String status) {
        this.email = email;
        this.status = status;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
